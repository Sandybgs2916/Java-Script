// arithmetic operator
console.log(3+3);
console.log(10-5);
console.log(20/5);
console.log(5*6);
console.log(83%8); // remainder
// Increment and decrement operator
// operator: x++ or ++x or x-- or --x
var num = 15
var newNum = num++;
console.log(num); // num assign by incrementing 1
console.log(newNum);
// comparison operator
// equal operator
var a = 30;
var b = 20;
console.log(a ==b);
// not equal operator
console.log(a!=b);
// greater than(>)
console.log(a > b);
// greater than or equal(>=)
console.log(a >= b);
// less than(<)
console.log(a < b);
// less than or equal to
console.log(a <= b);
// logical operator
// logical AND
var c = 10;
var d = -5;
console.log(c > d && d > 3);
console.log(c > d && d < 3);
// logical or
console.log(c > d || d > 3);
console.log(c < d && d > 3);
//not operator
console.log(!(c > d || d > 3));
console.log(!(c < d && d > 3));
console.log(!true);

