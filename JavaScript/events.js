// HTML Events
// An HTML event can be something the browser does, or something a user does.

// here are some examples of HTML events
// An HTML web page has finished loading
// An HTML input field was changed
// An HTML button was clicked

// JavaScript lets you execute code when events are detected.

// There are 4 ways of writing Events of javaScript
// 1: using alert(); simple inline alert
// 2: By calling a function
// 3: using Inline events (HTML onclick="" property and element.onclick)
// 4: using Event Listeners (addEventListener and IE's attachEvent)

// section 2: What is Event Object?
// Event object is the parent object of the event object.
// for example: MouseEvent, focusEvent, KeyboardEvent etc

// section 3: MouseEvent in JavaScript
// Events that occur when the mouse interacts with the html
// document belongs to the MouseEvent Object.

// section 4: KeyboardEvent in JavaScript 
// Events that occur when user presses a key on the keyboard, belongs to keyboardEvent

// section 5: InputEvents in JavaScript
// The change event occurs when the value of an element has been changed.
// For radiobuttons and checkboxes, the onchange event occurs when the checked
// state has been changed

// JavaScript Timing Events

// The window object allows execution of code at specified time intervals. 
// these time intervals are called timing events.

// Timing events methods are:
// 1. setTimeout()
// 2. clearTimeout()
// 3. setInterval()
// 4. clearInterval()

// setTimeout(function, milliseconds);
// executes a function, after waiting a specified number of milliseconds

// clearTimeout() clears the timeout THAT has been set by setTimeout() function.

// clearInterval() method used to stop any countdown or interval set by serInterval() method.

